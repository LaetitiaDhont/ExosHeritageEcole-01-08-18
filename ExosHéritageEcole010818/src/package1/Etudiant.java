package package1;
public class Etudiant extends Personne {
	
String classe;
String demiPensionnaire;
boolean externe;
boolean redoublant;
boolean boursier;
public String getClasse() {
	return classe;
}
public void setClasse(String classe) {
	this.classe = classe;
}
public String getDemiPensionnaire() {
	return demiPensionnaire;
}
public void setDemiPensionnaire(String demiPensionnaire) {
	this.demiPensionnaire = demiPensionnaire;
}
public boolean isExterne() {
	return externe;
}
public void setExterne(boolean externe) {
	this.externe = externe;
}
public boolean isRedoublant() {
	return redoublant;
}
public void setRedoublant(boolean redoublant) {
	this.redoublant = redoublant;
}
public boolean isBoursier() {
	return boursier;
}
public void setBoursier(boolean boursier) {
	this.boursier = boursier;
}





}
